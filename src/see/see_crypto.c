static const uint8_t rcon[] = {0x01, 0x02, 0x04, 0x08, 0x10,
                               0x20, 0x40, 0x80, 0x1b, 0x36};

/* Internal Helpers for Key Stretching */
static inline uint32_t rot_word(uint32_t w) { return (w >> 8) | (w << 24); }
static inline uint32_t sub_word(uint32_t w) {
  return (sbox[w & 0xFF]) | (sbox[(w >> 8) & 0xFF] << 8) |
         (sbox[(w >> 16) & 0xFF] << 16) | (sbox[(w >> 24) & 0xFF] << 24);
}

/* --- THE STRETCH FUNCTION --- */
void aes128_expand_key(const uint8_t *key, uint32_t *round_keys) {
  // Use your custom mem_copy
  mem_copy(round_keys, key, 16);

  for (int i = 4; i < 44; i++) {
    uint32_t temp = round_keys[i - 1];
    if (i % 4 == 0) {
      temp = sub_word(rot_word(temp)) ^ rcon[(i / 4) - 1];
    }
    round_keys[i] = round_keys[i - 4] ^ temp;
  }
}

/* --- HARDWARE ACCELERATED CORE --- */
static uint8x16_t encryption_keys[11];
static uint8x16_t decryption_keys[11];

void see_init_crypto(const uint8_t *master_key) {
  uint32_t raw_keys[44];
  aes128_expand_key(master_key, raw_keys);

  for (int i = 0; i < 11; i++) {
    uint8x16_t k = vld1q_u8((uint8_t *)&raw_keys[i * 4]);
    encryption_keys[i] = k;
  }

  decryption_keys[0] = encryption_keys[10];

  for (int i = 1; i < 10; i++) {
    decryption_keys[i] = vaesimcq_u8(encryption_keys[10 - i]);
  }

  decryption_keys[10] = encryption_keys[0];
}

wrapped_key_t see_wrap_key(uint64_t plain_key) {
  uint64_t block[2] = {plain_key, 0xFAEFA0A0A053FE00};
  uint8x16_t data = vld1q_u8((uint8_t *)block);

  // Rounds 0-8
  for (int i = 0; i < 9; i++) {
    data = vaeseq_u8(data, encryption_keys[i]);
    data = vaesmcq_u8(data);
  }

  // Round 9: XOR Key 9 + SubBytes/ShiftRows for Round 10
  data = vaeseq_u8(data, encryption_keys[9]);

  // Round 10: Final XOR
  data = veorq_u8(data, encryption_keys[10]);

  wrapped_key_t res;
  vst1q_u8((uint8_t *)&res, data);
  return res;
}

uint64_t see_unwrap_key(wrapped_key_t wrapped_key) {
  uint8x16_t data = vld1q_u8((uint8_t *)&wrapped_key);

  for (int i = 0; i < 9; i++) {
    data = vaesdq_u8(data, decryption_keys[i]);
    data = vaesimcq_u8(data);
  }

  data = vaesdq_u8(data, decryption_keys[9]);

  data = veorq_u8(data, decryption_keys[10]);

  return vgetq_lane_u64(vreinterpretq_u64_u8(data), 0);
}
